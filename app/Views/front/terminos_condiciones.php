<h3>
    ¿TE HA INTERESADO ALGUNOS DE NUESTROS PRODUCTOS?
</h3>
<h5>
    Comprarlo es muy fácil; te dejamos una guía orientativa para ayudarte en el proceso:
</h5>
<br>

<h5>
    1. Elegí el producto que querés comprar.
</h5>
<h5>
    2. Hacé clic en el botón "Agregar al carrito". Esto agregará el producto a tu carrito virtual.
</h5>
<h5>
    3. Si querés seguir comprando otros productos, agrégalos en este momento. Si ya estás listo para comprar, hacé clic en "Iniciar Compra".
</h5>
<h5>
    4. Completá tus datos de contacto y hacé clic en "Continuar".
</h5>
<h5>
    5. Ingresá la dirección en la querés recibir el producto y hacé clic en "Continuar".
</h5> 
<h5>
    6. Seleccioná el método de envío que prefieras y hacé clic en "Continuar".
</h5>
<h5>
    7. Elegí el medio de pago y hacé clic en "Continuar".
</h5>
<h5>
    8. En la página de Confirmación de compra podrás revisar toda la información detallada. Si los fatos ingresados son correctos, hacé clic en "Continuar".
</h5>
<h5>
    9. Serás redirigido a otra pantalla para que completes los datos sobre la forma de pago elegida.
</h5>
<h5>
    10. Después de confirmar la compra, recibirás un correo electrónico.
</h5>
<h5>
    11. Una vez acreditado el pago, haremos el envío correspondiente de los productos que hayas comprado.
</h5>
